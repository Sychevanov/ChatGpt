                        <h1 class="article__title">Diskussion um die skandalösesten Nachrichten dieser Woche </h1>
                        <h2 class="red">"Rentner sind ein Abfallmaterial, lassen Sie sie sterben, sie tun mir nicht leid. Wenn nicht der Blutdruck, also wird etwas anderes sie töten. Es macht keinen Sinn, Geld für alte Leute auszugeben!» </h2>
                        <p class="right"><i>Stellvertretender Chefapotheker </i></p>

                        <h2>Vor wenigen Tagen war in den sozialen Medien ein Screenshot des skandalösen Posts von Vanessa Pichler, der stellvertretenden Chefapothekerin, weit verbreitet. Nach der Bekanntgabe löschte sie ihren Facebook-Account. Aber wir haben noch einen Screenshot ihrer Aufnahme, den wir zeigen. </h2>

