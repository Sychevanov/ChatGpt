                                    <p><b>Gestern wurde bekannt, dass Vanessa Pichler und die gesamte Geschäftsführung der Pharma-Abteilung entlassen werden und dort eine Überprüfung durchgeführt wird. Um die Essenz des Geschehens zu verstehen, haben wir den Leiter der pharmazeutischen Abteilung, einen Vertreter der größten Apothekenkette Pharmastok, und den Leiter des Wissenschaftlichen Zentrums für Herz-Kreislauf-Chirurgie ins Studio eingeladen.</b></p>

                                    <figure class="Figure" style="text-align: center;">
                                        <img src="files/images/content-2.jpg">
                                        <span class="Figure-caption"></span>
                                    </figure>

                                    <p class="font">Moderator der Sendung. Wie kann eine solche Person in staatlichen Strukturen arbeiten? Auf wessen Kosten kaufte sie sich eine luxuriöse Villa und flog regelmäßig in teure Resorts in den Urlaub?</p>

                                    <p class="font">Warum kontrollieren solche Menschen das Schicksal eines einzigartigen Mittels des Instituts für Herz-Kreislauf-Pathologie, das Herz und Blutdruck heilt und den Tod verhindert? Und das sollte allen Bürgern zur Verfügung stehen, nicht nur den Reichen. Das werden wir heute diskutieren.</p>

                                    <p><b>Moderator: "Die erste Frage geht an Raul Schmidt, den Leiter der pharmazeutischen Abteilung. Herr Schmidt, wie konnte so eine Person auf so einer hohen Stellung sein?</b></p>


                                    <figure class="Figure" style="text-align: center;">
                                        <img src="files/images/content-3.jpg">
                                        <span class="Figure-caption"></span>
                                    </figure>


                                    <p><b><u>Raul Schmidt:</u></b> Damit Sie meine Situation verstehen, bin ich erst vor drei Tagen zum Abteilungsleiter ernannt worden. Die ganze alte Führungsspitze wurde entlassen, einschließlich der fraglichen Frau. Also, zu diesem Zeitpunkt ist sie nicht mehr im Dienst und hat nichts mit der Medizin zu tun.</p>

                                    <p>Der Grund für die Massenentlassung war, wie Sie selbst verstehen, der Verdacht auf Korruption. Meine Aufgabe ist es, alle getroffenen Entscheidungen zu prüfen. Schon jetzt kann ich sagen, dass eine ganze Gruppe von Entscheidungen der alten Führung überarbeitet werden muss. </p>

                                    <p><b>Moderator: "Das heißt, sie haben nicht nur sie gefeuert, sondern das gesamte Management? Welche Lösungen meinen Sie? Können Sie ein Beispiel nennen?"</b></p>

                                    <p><b><u>Raul Schmidt:</u></b> Ja, die gesamte alte Abteilungsleitung wurde vollständig entlassen. Als Beispiel können Sie die Frage anführen, die in dem Screenshot aufgeworfen wird. Dort diskutieren sie über eine Initiative zur Verbreitung eines neuen Mittels zur Behandlung von Bluthochdruck unter Rentnern. Gemäß dieser Initiative wurde geplant, ein spezielles Programm innerhalb des Landes zu starten, nach dem Leute das Mittel zum minimalen Preis bekommen konnten. Es war geplant, seine Ausfuhr zu verbieten, da das Präparat nicht genug ist.</p>

                                    <p>Aber die alte Führung traf genau das Gegenteil. Sie blockierten die Idee eines erschwinglichen Mittels für ihre eigenen Bürger und zogen es vor, es im Ausland zu verkaufen. Es ist ihnen sogar gelungen, einen Vertrag mit Frankreich über die Lieferung des Mittels zu unterzeichnen. Zum Preis von 2.500,- Euro pro Packung. Dies trotz der Tatsache, dass sie nicht einmal ein solches Recht hatten, da die Rechte an dem Mittel dem Institut für Herz-Kreislauf-Pathologien gehören</p>

                                    <p><b>Moderator: "Wow! Das ist aber der Preis! 2500 Euro pro Packung. Warum so teuer? Und wie sollten sie es zum niedrigsten Preis verkaufen, das Budget wird solche Ausgaben einfach nicht decken können?"</b></p>

                                    <p><b><u>Raul Schmidt:</u></b> 2.500,- Euro, das sind die Kosten, die sie den Franzosen in Rechnung gestellt haben, und die haben zugestimmt. Hier hatte Frau Pichler Recht, im Ausland sind alle wirklich bereit, fast jedes Geld zu zahlen. </p>

                                    <p>Der Grund ist einfach - es ist das einzige Mittel weltweit, das das Herz-Kreislauf-System einer Person vollständig heilen und in einen gesunden Zustand zurückkehren kann. Sie selbst wissen sicher, dass Herzerkrankungen die Ursache für zwei Drittel der Todesfälle auf der Welt sind. Das Präparat befreit eine Person vollständig von ihnen. Ohne Operation und Todesrisiko auf dem OP-Tisch. Der Blutdruck einer Person hört auf zu steigen, die Gefäße werden gesund, was bedeutet, dass SIE VIEL LÄNGER LEBEN WIRD.</p>

                                    <p>Die Produktionskosten des Mittels betragen meines Wissens 110 Euro. Der größte Teil dieses Betrags sollte aus dem medizinischen Fonds entschädigt werden, und das Mittel selbst sollte zu minimalen Kosten an Personen im Rahmen des Programms verkauft werden. </p>

                                    <p><b>Moderator: "Eine Frage an Peter Flückiger - was ist mit diesem Mittel, dass die Ausländer bereit sind, es zu einem solchen Preis zu kaufen? 2500,- Euro pro Packung, das ist wahnsinnig teuer".</b></p>

                                    <figure class="Figure" style="text-align: center;">
                                        <img src="files/images/content-4.jpg">
                                        <span class="Figure-caption"></span>
                                    </figure>


                                    <p><b><u>Peter Flückiger:</u></b> Es ist ein Präparat, an dem das Team des Instituts für kardiovaskuläre Pathologien seit mehr als zehn Jahren arbeitet. Das Ergebnis ist ein einzigartiges Mittel, das das menschliche Herz-Kreislauf-System vollständig verbessern kann. Grob gesagt, es bringt es in seinen ursprünglichen Zustand zurück. Auch wenn eine Person schwere Herz- und Gefäßerkrankungen hat. Herzinsuffizienz, Schlaganfall und Herzinfarkt sind weltweit die Ursache für zwei Drittel der Todesfälle. Das Mittel verhindert alle drei Pathologien und den frühen Tod einer Person. Daher kommt eine solche verrückte Nachfrage.</p>

                                    <p><b>Moderator: "Und was genau macht es?"</b></p>

                                    <p><b><u>Peter Flückiger:</u></b> Es ist ein komplexes Mittel, das einmal auf das gesamte Herz-Kreislauf-System wirkt und gleichzeitig mehrere Aufgaben löst.</p>

                                    <p><b>Es reinigt die Gefäße vollständig von Verschmutzung und löst Blutgerinnsel.</b> DANK EINER EINZIGARTIGEN SUBSTANZ – ALPHAHYDROCALCIFEROL. FÜR DIE ENTDECKUNG DIESER SUBSTANZ ERHIELTEN KANADISCHE WISSENSCHAFTLER 2011 DEN NOBELPREIS. Cholesterin, Fettschichten, Korken aus Blutgerinnseln - all das gehört der Vergangenheit an. Wichtig ist, dass auch die ischämische Arterie gelöscht wird, was das Risiko eines Myokardinfarkts auf Null reduziert.</p>

                                    <p><b>Es startet den Prozess der Regeneration des Herzmuskels.</b> Die beschädigten Bereiche werden wiederhergestellt, die Herzklappen normalisieren sich wieder und leiden normalerweise stark unter Bluthochdruck.</p>

                                    <p><b>Es repariert beschädigte Gefäßwände und stärkt sie.</b> Das Risiko von Gefäßrissen und Hirnblutungen wird verhindert. Im Allgemeinen werden die Gefäße völlig gesund und elastisch, wie in der Jugend. DAS MITTEL ENTHÄLT ÜBER 50 MAKRO- UND MIKROELEMENTE, DIE FÜR HERZ UND BLUTGEFÄßE NÜTZLICH SIND, UND AUCH VITAMINE.</p>

                                    <p>Als Ergebnis gibt es eine vollständige Wiederherstellung des menschlichen Herz-Kreislauf-Systems. Und der Mensch selbst kehrt in einen absolut gesunden Zustand zurück. Wie mehrstufige klinische Studien bestätigt haben, hilft das Mittel auch im Falle einer extrem schweren Erkrankung des Patienten. Wenn Sie gleichzeitig an einer koronaren Herzkrankheit leiden, Ihr Blutdruck steigt, treten Arrhythmien und Tachykardien auf, hilft das Präparat trotzdem. Ein wichtiger Punkt ist, dass Bluthochdruck vollständig beseitigt wird. Das ist die Ursache aller Erkrankungen des Herzens und der Blutgefäße.</p>

                                    <p><b>Moderator: "Können Sie hier abbrechen? Hoher Blutdruck ist bei den meisten Menschen häufig. Und Sie sagen, dass es die Ursache von Herz- und Gefäßerkrankungen ist. Erklären Sie das?" </b></p>

                                    <p><b><u>Peter Flückiger:</u></b> Deshalb werden fast 70% der Todesfälle in unserem Land durch Erkrankungen des Herzens und der Blutgefäße verursacht. Weil jeder denkt, dass der hohe Blutdruck normal ist.</p>

                                    <p>Darüber hinaus beginnen sich aufgrund des erhöhten Blutdrucks alle Erkrankungen des Herz-Kreislauf-Systems zu entwickeln. Und alle Todesfälle aufgrund von Herzinsuffizienz, Herzinfarkt oder Schlaganfall sind auf Blutdruck zurückzuführen. </p>

                                    <p><b>Moderator: "Warum ist es so gefährlich?"</b></p>

                                    <p><b><u>Peter Flückiger:</u></b> Wenn man es ganz kurz formuliert, dann beginnt der Blutdruck aufgrund der Stagnation des Blutes in den kleinen Arterien zu steigen. Der Körper muss das Blut mit der erforderlichen Geschwindigkeit weiter drücken und liefert somit die erforderliche Geschwindigkeit. </p>

                                    <p>Aufgrund von Sprungdruck werden die Wände großer Arterien beschädigt. Cholesterin und andere Fettplaques beginnen sich an den Schadensstellen abzusetzen. Atherosklerose tritt auf. </p>

                                    <p>Moment Nr. 1 - Dies führt zu einer koronaren Herzkrankheit. Moment Nr. 2 - Die Intensität von Sprungdruck beim Menschen nimmt zu. Was wiederum zu einer noch größeren Verunreinigung der Gefäße führt. </p>

                                    <p>Gleichzeitig wächst die Belastung des Herzens, die sich um ein Vielfaches schneller abnutzt als es sollte. Es treten Probleme mit Herzklappen auf, erscheinen Arrhythmien und Tachykardie. Der oft hohe Blutdruck erhöht das Risiko eines Blutgefäßbruchs und eines Schlaganfalls. Eine Person kriegt sofort eine ganze Reihe tödlicher Krankheiten, die anfänglich allein durch Bluthochdruck verursacht werden. Sie kann jederzeit sterben. </p>

                                    <p><b>Moderator: "Wie hoch ist die Wahrscheinlichkeit eines frühen Todesfalls? Wenn ich zum Beispiel Drucksprünge habe, wie lange dauert es dann vom Beginn der Krankheit bis zum Tod?"</b></p>

                                    <p><b><u>Peter Flückiger:</u></b> Wenn man die Behandlungsmethoden der Patienten berücksichtigt, die die meisten Ärzte verwenden und die Präparate, die ihnen verschrieben werden, dann ist die Sterblichkeitswahrscheinlichkeit alle 100%. Leider gibt es derzeit in Deutschischen Apotheken einfach keine wirksamen Mittel. </p>

                                    <p>Myokardinfarkt, akute Herzinsuffizienz, Schlaganfall - etwas davon wird sicher eine Person treffen und töten. Die Zeit vom Auftreten des ersten Drucksprungs bis zum Tod ist bei allen unterschiedlich und hängt von individuellen Faktoren ab. Viel hängt vom Lebensstil, den eingenommenen Präparaten und Ihrer Vererbung ab. </p>

                                    <p>Im Durchschnitt kann ich mit Sicherheit sagen, dass erhöhter Blutdruck und andere Erkrankungen des Herz-Kreislauf-Systems das Leben einer Person um 15-20 Jahre verkürzen, nicht weniger. Jemand, zum Beispiel, könnte bis zu 80 leben, stirbt aber mit 60. Das Gegenteil ist wahr, wenn Sie die Gefäße und das Herz rechtzeitig verbessern, können Sie viel länger leben.</p>

                                    <p><b>Moderator: "Und wie kann man sich retten? Gibt es wirksame Behandlungsmöglichkeiten? Können Sie sich auf die Hilfe von Ärzten verlassen?"</b></p>

                                    <p><b><u>Peter Flückiger:</u></b> Es hängt von der konkreten Situation ab. Sie wissen, dass die Masse der Ärzte sitzt, in der Tat, auf dem Gehalt von Pharmaunternehmen. In diesem Fall sind viele von ihnen nicht einmal bewusst, dass sie dem Patienten helfen, zu sterben, wenn sie bestimmte Medikamente verschreiben. Sie stellen sich nicht vor, wie gefährlich sie sind.</p>

                                    <p>Die andere Seite des Problems - Apothekenketten, wo es praktisch keine wirksamen Präparate gibt. Apotheken sind schließlich ein Unternehmen, das an maximalem Gewinn interessiert ist. Sie sind nicht an neuen Mitteln interessiert, die tatsächlich helfen könnten. Darunter die Entwicklung des Instituts für kardiovaskuläre Pathologien, auf die alle bis zu einer Apotheke in Deutsch verzichtet haben. </p>

                                    <p>Unmittelbar danach beschloss die Leitung des Instituts, ein Programm zu starten, bei dem jeder Mensch unabhängig vom Einkommensniveau ein hochwirksames Mittel zur Behandlung von Herz und Blutgefäßen erwerben kann. Sie erhielten jedoch eine Ablehnung von Frau Pichler, die auch den Verkauf von "Cardione" im Ausland erzwingen wollte, was im Allgemeinen nicht vernünftig ist. Wir haben zehn Jahre gearbeitet, um den Bürgern Deutschs zu helfen, nicht den Franzosen oder Amerikanern. </p>

                                    <p><b>Moderator: Bei uns im Studio ist der Vertreter der Apothekenkette "Pharmastok" Christopher Klein anwesend. Die Frage ist - warum haben Sie sich aufs Mittel "Cardione" verzichtet? </b></p>


                                    <figure class="Figure" style="text-align: center;">
                                        <img src="files/images/content-5.jpg">
                                        <span class="Figure-caption"></span>
                                    </figure>


                                    <p><b><u>Christopher Klein:</u></b> Sie boten keine Geschäftspartnerschaft an, sondern eine Art Wohltätigkeit. Das Mittel mit einem maximalen Aufschlag von 10% zu verkaufen. Darüber hinaus wollten sie den Kaufpreis schrittweise senken, indem sie medizinische Stiftungen anziehen. Was ist denn das? Natürlich tut es mir leid, aber wir haben Privatunternehmen. Und wenn das Angebot nicht kommerziell rentabel ist, haben wir jedes Recht, es abzulehnen.</p>

                                    <p><b><u>Peter Flückiger:</u></b> Sie haben nicht nur ein Privatunternehmen. Das Leben der Menschen hängt davon ab. Schließlich kaufen sie die Mittel von ihnen. Sie verkaufen Medikamente, von denen bekannt ist, dass sie unwirksam sind, und weigern sich, alles zu verkaufen, was ihrem Geschäftsergebnis schaden könnte. Ich war persönlich bei den Verhandlungen und hörte Ihre Mitarbeiter sagen, dass unser "Cardione" den Verkauf aller Herz-und Bluthochdruckmedikamente, die zwei Drittel Ihres Gewinns ausmachen, stürzen würde. Es ist widerlich und schrecklich, mit dem Leben der Menschen Geld zu verdienen!</p>

                                    <p><b>Moderator: Herr Klein, können Sie das irgendwie kommentieren?</b></p>

                                    <p><b><u>Christopher Klein:</u></b> Ich bin mir nicht sicher, ob es sich lohnt zu antworten. Ich kann nur sagen, dass alle Mittel, die wir verkaufen, zertifiziert wurden. Wenn einer von ihnen verboten ist, werden wir es sofort aus dem Verkauf nehmen. Aber alles wird streng nach dem Gesetz gemacht. Was "Cardione" vom Institut für Herz-Kreislauf-Pathologien betrifft, so haben wir, wie gesagt, ein Privatunternehmen. Und wir können nicht gezwungen werden, etwas gegen unseren Willen zu verkaufen. </p>

                                    <p><b><u>Peter Flückiger:</u></b> Während Leute wie Frau Pichler in der pharmazeutischen Abteilung gearbeitet haben, kann ich mir vorstellen, wie sie das Zertifizierungsverfahren für neue Mittel durchlaufen haben. Ich hoffe, dass sich die neue Führung auch mit der Überprüfung der anwesenden Vorbereitungen beschäftigen wird. </p>

                                    <p><b>Moderator: Raul, liegt "Cardione" immer noch auf Lager?</b></p>

                                    <p><b><u>Raul Schmidt:</u></b> Zum Glück nicht. Wir sind jetzt von der Bereinigung der administrativen Probleme des vorherigen Teams zur normalen Arbeit übergegangen. Und wir haben gemeinsam mit dem Institut für kardiovaskuläre Pathologien sowie der medizinischen Stiftung die direkte Verbreitung von "Cardione" etabliert.</p>

                                    <p>Außerdem haben wir eine Vereinbarung über die Finanzierung seiner Produktion aus dem Budget getroffen, wie sie ursprünglich vorgeschlagen haben. </p>

                                    <p>Ich möchte, dass Sie verstehen, das Institut für kardiovaskuläre Pathologien keine kommerzielle Struktur ist. Deshalb hat es kein Ziel, Geld zu verdienen. Im Gegenteil, für dieses Mittel wird Geld aus dem Budget ausgegeben. Als Ergebnis gab es die Möglichkeit, "Cardione" zu einem minimalen Preis zu verteilen. Jeder Deutschische Staatsbürger kann das Mittel mit der Lieferung bestellen. </p>

                                    <h2><u>Der Preis für das einzigartige Mittel "Cardione" im Rahmen des Programms beträgt nur 49 Euro! (das ist sogar niedriger als sein Selbstkostenpreis, der 110 Euro beträgt)</u></h2>

                                    <p><b>Moderator: Wie können Menschen Cardione bekommen? Wo? Warum schickten Sie es nicht in Krankenhäuser?</b></p>

                                    <p><b><u>Raul Schmidt:</u></b> Wir schickten es aus Angst vor banalem Diebstahl nicht in Krankenhäuser. In dem Screenshot, den Sie am Anfang zitiert haben, schlägt einer der Kommentatoren tatsächlich vor, "Cardione" zu stehlen und ins Ausland zu verkaufen. Darüber hinaus lädt er andere zur Zusammenarbeit ein. Wenn wir das Mittel in Krankenhäuser schicken, werden wir die Situation außer Kontrolle bringen. Und wie meine Erfahrung zeigt, wird unser Mittel wahrscheinlich nur an diejenigen ausgegeben, die gut in die Tasche des Arztes zahlen können.</p>

                                    <p>Daher erfolgt die Ausgabe unter strenger Aufsicht des Instituts für kardiovaskuläre Pathologien. Zusätzlich wird jeder Versand des Mittels von einer Sonderkommission überwacht. Um "Cardione" zu erhalten, genügt es, einen Antrag mit Ihrem Namen und Ihrer Telefonnummer zu hinterlassen. </p>

                                    <p><b>Moderator: Ich habe richtig verstanden - jeder Deutschische Staatsbürger kann ein Mittel zur Behandlung von Herzerkrankungen, das 110 Euro zum Selbstkostenpreis kostet, für nur 49 Euro bekommen?</b></p>

                                    <p><b><u>Raul Schmidt:</u></b> Ja, jeder Deutschische Staatsbürger kann einen Antrag auf "Cardione" stellen und es für 49 Euro bekommen. </p>

                                    <p>Es gibt ein "aber" - dies wird durch Subventionen erreicht. Bisher wird es genügend Mittel für eine Lieferung von 5.500 Packungen des Mittels geben. Die nächste Lieferung wird bereits ohne Finanzierung für die Selbstversorgung sein, so dass der Preis dort fast 4-5 Mal höher sein wird! Die geschätzten Kosten für die nächste Lieferung für die Deutschischen Einwohner werden 150-200 Euro betragen.</p>

                                    <p><b>Moderator: Aber bislang können die Leute das Mittel noch für 49 Euro bekommen? </b></p>

                                    <p><b><u>Raul Schmidt:</u></b> Jetzt sind von der ersten Lieferung noch rund 1500 Packungen übrig. Die Nachfrage wächst exponentiell. Wenn am ersten Tag nur hundert Pakete verkauft wurden, dann am dritten Tag mehr als tausend. Daher ist es schwer zu sagen, wie lange das Mittel zu einem so niedrigen Preis verfügbar sein wird. Höchstwahrscheinlich 1-2 Tage. Dann wird es nur noch teurer. </p>


